class EffectOnProtection:
    @staticmethod
    def effect_on_protection(protection: int, effect: int) -> int:
        return protection + effect


class EffectOnPower:
    @staticmethod
    def effect_on_power(power: int, effect: int) -> int:
        return power + effect


class EffectOnHp:
    @staticmethod
    def effect_on_hp(hp: int, effect: int) -> int:
        return hp + effect
